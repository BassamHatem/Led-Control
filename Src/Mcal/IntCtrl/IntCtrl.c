void IntCtrl_Init(const IntCtrl_ConfigType* ConfigPtr)
{

	/*TODO Configure Grouping\SubGrouping System in APINT register in SCB*/
    APINT = 0xFA05|0x00001234;
    
    /*TODO : Assign Group\Ssubgroup priority in NVIC_PRIx Nvic and SCB_SYSPRIx Registers*/  


	/*TODO : Enable\Disable based on user configurations in NVIC_ENx and SCB_Sys Registers */
	
	if (ConfigPtr->ISR < 32)
		SET_BIT(*INT_EN(0),ConfigPtr->ISR);
	else if ((ConfigPtr->ISR >= 32) && (ConfigPtr->ISR < 64))
		SET_BIT(*INT_EN(1),(ConfigPtr->ISR - 32));
	else if ((ConfigPtr->ISR >= 64) && (ConfigPtr->ISR < 96))
		SET_BIT(*INT_EN(2),(ConfigPtr->ISR - 64));	
	else if ((ConfigPtr->ISR >= 96) && (ConfigPtr->ISR < 128))
		SET_BIT(*INT_EN(3),(ConfigPtr->ISR - 96));
	else if ((ConfigPtr->ISR >= 128) && (ConfigPtr->ISR < 138))
		SET_BIT(*INT_EN(4),(ConfigPtr->ISR - 128));

}

