#define Port_PinType		uint8

/**********************************************************************************************************************
 *  GLOBAL DATA TYPES AND STRUCTURES
 *********************************************************************************************************************/
typedef enum
{
	NORMAL_MODE, SPECIAL_MODE
}Port_PinMode;

typedef enum
{
	INPUT, OUTPUT
}Port_PinDirection;

typedef enum
{
	PULL_UP, PULL_DOWN, OPEN_DRAIN
}PortPinInternalAttach;

typedef enum
{
	R2R, R4R, R8R
}PortPinOutputCurrent;

typedef struct
{
	Dio_PortType Port;
	Dio_ChannelType Pin;
}Port_ChannelType;

typedef struct
{
	Port_ChannelType Channel;
	Port_PinMode Mode;
	Dio_LevelType Value;
	Port_PinDirection Direction;
	PortPinInternalAttach Attach;
	PortPinOutputCurrent Current;
}Port_ConfigType;
 

